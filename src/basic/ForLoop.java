package basic;

public class ForLoop {

	public static void main(String[] args) {
		
		// i++ = i+1;
		// Print 1 to 10
		// Initialization, condition and Increment
		for (int i = 1; i <= 10 ; i++) {
			System.out.println(i);
		}

		System.out.println("*******************");
		
		// Initialization, condition and Decrement
		for(int j = 10; j>=1; j--) {
			System.out.println(j);
		}
	}

}
