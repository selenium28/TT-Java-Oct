package basic;

public class LoopsConcept {

	public static void main(String[] args) {
				
		// Disadvantage of while loop: without increment partit will goes in infinite.
		int a = 1;  //  Initialization 
		while(a<=10) {  // Condition
			System.out.println(a);
			a = a+1;    // Increment 
		}
		
	}

}
